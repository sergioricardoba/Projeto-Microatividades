# 1. Crie uma variável chamada temperatura e atribua a ela o valor 29
# 2. Crie uma verificação, utilizando a condição if, para checar se o valor da variável temperatura é menor que 30
# 3. Caso positivo, imprima na tela o texto ‘A temperatura hoje está amena’
# 4. Caso contrário, e utilizando a condição else, imprima na tela o texto ‘Hoje está fazendo calor’

